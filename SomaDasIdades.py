'''
***Problema***
Crie um programa que ao analise uma Lista [15,46,75,37,23]
Realize a soma de cada item da lista, informando ao final a soma final das idades.
Utilizando o Metodo 5Qs
Qual os dados de entrada?
Oque fazer com esses dados?
Qual as restrições do programa?
Qual o resultado esperado?
Qual a sequência de passo a passo em Pseudocódigo?
'''
'''Solução'''

idade = [15,46,75,34,23]
total = 0
for i in idade:
    total = total + i
print(total)